public class Point {

    private int pX;
    private int pY;

    public Point(int pX, int pY) {
        this.pX = pX;
        this.pY = pY;
    }

    public int getpX() {
        return pX;
    }

    public void setpX(int pX) {
        this.pX = pX;
    }

    public int getpY() {
        return pY;
    }

    public void setpY(int pY) {
        this.pY = pY;
    }


}
